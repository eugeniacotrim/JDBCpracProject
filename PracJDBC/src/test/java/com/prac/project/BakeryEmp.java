package com.prac.project;

public class BakeryEmp {

	Integer emp_id;
	String emp_name;
	Integer emp_age;
	String emp_email;
	String emp_submission;
	Integer emp_salary;
	
	
	public BakeryEmp() {
	}
	
	
	public Integer getEmp_id() {
		return emp_id;
	}
	
	public void setEmp_id(Integer emp_id) {
		this.emp_id = emp_id;
	}
	
	public String getEmp_name() {
		return emp_name;
	}
	
	public void setEmp_name(String emp_name) {
		this.emp_name = emp_name;
	}
	
	public Integer getEmp_age() {
		return emp_age;
	}
	
	public void setEmp_age(Integer emp_age) {
		this.emp_age = emp_age;
	}
	
	public String getEmp_email() {
		return emp_email;
	}
	
	public void setEmp_email(String emp_email) {
		this.emp_email = emp_email;
	}
	
	public String getEmp_submission() {
		return emp_submission;
	}
	
	public void setEmp_submission(String emp_submission) {
		this.emp_submission = emp_submission;
	}
	
	public Integer getEmp_salary() {
		return emp_salary;
	}
	
	public void setEmp_salary(Integer emp_salary) {
		this.emp_salary = emp_salary;
	}	
}